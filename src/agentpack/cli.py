@app.command()
def generate(
    path: Optional[Path] = typer.Argument(
        None,
        help="Target directory. Defaults to current directory.",
    ),
    force: bool = typer.Option(
        False,
        "--force",
        help="Overwrite files modified outside agentpack.",
    ),
):
    """Compile canonical rulesets into tool-specific configs."""
    root = (path or Path.cwd()).resolve()
    ap_dir = root / AGENTPACK_DIR

    if not ap_dir.exists():
        typer.echo("Not initialized. Run `agentpack init` first.", err=True)
        raise typer.Exit(code=1)

    config = _load_config(ap_dir)
    agents = config.get("agents", [])
    use_gitignore = config.get("gitignore", True)

    if not agents:
        typer.echo("No agents configured in agentpack.yaml", err=True)
        raise typer.Exit(code=1)

    typer.echo("Generating...")
    _cleanup_stale_generated(root, agents)

    if "claude" in agents:
        typer.echo("Claude:")
        _generate_claude(root, ap_dir, force)

    if "cursor" in agents:
        typer.echo("Cursor:")
        _generate_cursor(root, ap_dir, agents, force)

    if use_gitignore:
        _update_gitignore(root, agents)

    typer.echo("Done.")


@app.command()
def sync():
    """Pull shared rules and skills from configured remote repositories."""
    cache_root = Path.home() / ".cache" / "agentpack" / "remotes"
    root = Path.cwd().resolve()
    ap_dir = root / AGENTPACK_DIR

    if not ap_dir.exists():
        typer.echo("Not initialized. Run `agentpack init` first.", err=True)
        raise typer.Exit(code=1)

    config = _load_config(ap_dir)
    remotes = config.get("remotes") or {}

    if not remotes:
        typer.echo("No remotes configured. Add remotes to agentpack.yaml to use sync.")
        return

    errors: list[str] = []
    for name, value in remotes.items():
        url, remote_path = _parse_remote_config(value)
        typer.echo(f"Syncing {name}...")
        try:
            _sync_remote(name, url, remote_path, cache_root, ap_dir)
            typer.echo("  Done.")
        except Exception as e:
            errors.append(f"{name}: {e}")
            typer.echo(f"  ERROR: {e}", err=True)

    if errors:
        typer.echo("\nSync completed with errors:", err=True)
        for e in errors:
            typer.echo(f"  {e}", err=True)
        raise typer.Exit(code=1)
    else:
        typer.echo("Done.")
